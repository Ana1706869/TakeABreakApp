     public static boolean isValidEmail(String email) {
        String regex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(email);
        return matcher.matches();
     }
     
      public static boolean validarCodigoPostal(String codigoPostal) {
        // Expressão regular para validar código postal (XXXXX-XXX)
        String regex = "^\\d{4}-\\d{3}$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(codigoPostal);
        return matcher.matches();
    }
    private void inserirFuncionario()  {
        // Consulta SQL para inserir dados
        String sql = "INSERT INTO Funcionário (Nome, Departamento, DataNascimento, Endereço, CódigoPostal, Localidade, Concelho, Distrito, Email, Telefone, DataAdmissão, Password, Escalao) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

        try {
            this.connector();
            // Preparar a consulta
            statement = con.prepareStatement(sql);
            // Preencher os parâmetros da consulta com os dados dos campos
            statement.setString(1, jTextFieldNome.getText().trim());
            statement.setString(2, jComboBoxDepartamento.getSelectedItem().toString());
            
            SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
            Date dataNascimentoSelecionada=jDateChooserDataNascimento.getDate();
            if(dataNascimentoSelecionada!=null){
               
                String dataNascimento=sdf.format(dataNascimentoSelecionada);
               statement.setString(3,dataNascimento); 
            }else{
                statement.setNull(3,Types.DATE);
            }
            
            
            statement.setString(4, jTextFieldEndereco.getText().trim());
            statement.setString(5, jFormattedTextFieldCodigoPostal.getText().trim());
            statement.setString(6, jTextFieldLocalidade.getText().trim());
            statement.setString(7, jTextFieldConcelho.getText().trim());
            statement.setString(8, jTextFieldDistrito.getText().trim());
            statement.setString(9, jFormattedTextFieldEmail.getText().trim());
            statement.setString(10, jFormattedTextFieldTelefone.getText());
            
            
            sdf=new SimpleDateFormat("dd/MM/yyyy");
            Date dataAdmissaoSelecionada=jDateChooserDataAdmissao.getDate();
            if(dataAdmissaoSelecionada!=null){
               
                String dataAdmissao=sdf.format(dataAdmissaoSelecionada);
               statement.setString(11,dataAdmissao); 
            }else{
                statement.setNull(11,Types.DATE);
            }
            
            
            
            statement.setString(12, jPasswordField1.getText().trim());
            statement.setString(13, jComboBoxEscalao.getSelectedItem().toString());
            

            // Executar a consulta
            statement.executeUpdate();

            // Exibir mensagem de sucesso
            JOptionPane.showMessageDialog(this, "Parabéns! Está registado na nossa aplicação.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            Login login = new Login(new javax.swing.JFrame(), true);
                login.setVisible(true);
                
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao inserir dados na base de dados: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        
        }finally{
        try{
            statement.close();
            con.close();
        }catch(Exception e){
            
        }
        
        }
        
    }
    
    
    
    

