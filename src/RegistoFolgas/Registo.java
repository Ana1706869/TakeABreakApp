    private void jButtonRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistarActionPerformed
        String nome = jTextFieldNome.getText().trim();
    String email = jFormattedTextFieldEmail.getText().trim();
    String telefone = jFormattedTextFieldTelefone.getText().trim();
    String password = jPasswordField1.getText().trim();
    String verificacaoPassword = jPasswordField2.getText().trim();
    Date dataAdmissao=jDateChooserDataAdmissao.getDate();
    
    if (nome.isEmpty() || email.isEmpty() || telefone.isEmpty() || dataAdmissao==null|| password.isEmpty() || verificacaoPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos obrigatórios", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
           
            
                
                if (!isValidEmail(email)) {
                   
   
                    JOptionPane.showMessageDialog(this, "E-mail inválido", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
                }
                
                String codigoPostal = jFormattedTextFieldCodigoPostal.getText().trim();
                if (!validarCodigoPostal(codigoPostal)&& (!codigoPostal.isEmpty())) {
                    
  
                    JOptionPane.showMessageDialog(this, "Código postal inválido", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                
                if(telefone.length()<9){
                   JOptionPane.showMessageDialog(this, "Número de telefone inválido! O número de telefone tem de ter 9 dígitos", "Erro", JOptionPane.ERROR_MESSAGE); 
            return;
        }
            

           
            // Verificar se as senhas coincidem
            if (!password.equals(verificacaoPassword)) {
                JOptionPane.showMessageDialog(this, "As palavras-passe não coincidem", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
            }
               
          inserirFuncionario();
     

    
    
   
    }//GEN-LAST:event_jButtonRegistarActionPerformed
    
